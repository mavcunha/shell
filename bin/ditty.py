# maps fingers to letters
# reason: easier to write and read
FINGERS = {
    'li': ['r', 'f', 'c', 't', 'g', 'v', 'b'],
    'lm': ['e', 'd', 'c'],
    'lr': ['w', 's', 'x'],
    'lp': ['q', 'a', 'z'],
    'ri': ['y', 'h', 'n', 'u', 'j', 'm'],
    'rm': ['i', 'k'],
    'rr': ['o', 'l'],
    'rp': ['p']
}

# reverse maps letters to finger
# reason: quicker access during scoring
R_FINGERS={}
for k in FINGERS:
    for l in FINGERS[k]:
        R_FINGERS[l]=k

# reason: common op, easier read
def same_finger(a, b):
    return R_FINGERS[a] == R_FINGERS[b]

# reason: easier read
def different_finger(a, b):
    return not same_finger(a, b)

# reason: common op, easier read
def same_hand(a, b):
    return R_FINGERS[a][0] == R_FINGERS[b][0]

# reason: easier read
def different_hand(a, b):
    return not same_hand(a, b)

# reason: common op, find that a pair will be 
# typed with the left hand
def left_hand(a, b):
    return same_hand(a, b) and R_FINGERS[a][0] == 'l'

def right_hand(a, b):
    return same_hand(a, b) and R_FINGERS[a][0] == 'r'

# reason: encapsulate a basic scoring
# which just discount if the same finger
# is used consecutive times
def basic(word):
    sum = 0
    w = word.strip()
    for a, b in zip(w, w[1:]):
        if same_finger(a, b):
            sum = sum - 1
        else:
            sum = sum + 1
    return sum / len(word)

# reason: encapsulate a hand alternating scoring
# where alternation of hands scores higher
def hand_alternation(word):
    sum = 0
    w = word.strip()
    for a, b in zip(w, w[1:]):
        if same_hand(a, b) and same_finger(a, b):
            sum = sum - 2
        elif same_hand(a, b):
            sum = sum - 1
        else:
            sum = sum + 1
    return sum / len(word)

# reason: favors left hand typing
def left(word):
    sum = 0
    w = word.strip()
    for a, b in zip(w, w[1:]):
        if left_hand(a, b) and different_finger(a, b):
            sum = sum + 2
        elif same_hand(a, b):
            sum = sum + 1
        else:
            sum = sum - 1
    return sum / len(word)

# reason: favors right hand typing
def right(word):
    sum = 0
    w = word.strip()
    for a, b in zip(w, w[1:]):
        if right_hand(a, b) and different_finger(a, b):
            sum = sum + 2
        elif same_hand(a, b):
            sum = sum + 1
        else:
            sum = sum - 1
    return sum / len(word)

# reason: main routine, go through the words 
# sorting it using the scoring algorithm
def run(limit):
    count = 0
    for i in sorted(WORDS, key=right, reverse=True):
        count = count + 1
        print(i.strip(), end=' ')
        if count >= limit:
            print()
            break

